package gdts.common.data;

/**
 * Role, które przyjmuje dana zmienna.
 * 
 * @author Maciej Milczarek
 */
public enum VarRole {

	/**
	 * Rola domyślna.
	 */
	STANDARD,
	
	/**
	 * Rola wejściowa.
	 */
	INPUT,
	
	/**
	 * Rola wyjściowa.
	 */
	OUTPUT,
	
	/**
	 * Rola jednocześnie wejściowa i wyjściowa.
	 */
	INOUT
}
